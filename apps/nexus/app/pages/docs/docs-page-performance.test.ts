    expect(page).toContain('function seedFullDocFromCurrentDoc()')
    expect(page).toMatch(/function seedFullDocFromCurrentDoc\(\) \{[\s\S]*if \(!shouldSplitDocBody\.value \|\| !doc\.value\?\.body\)[\s\S]*return false[\s\S]*fullDoc\.value = cacheFullDoc\(doc\.value as Record<string, any>\)[\s\S]*return true[\s\S]*\}/)
    expect(page).toContain('function startFullDocFetchForRoute()')
    expect(page).toMatch(/function startFullDocFetchForRoute\(\) \{[\s\S]*if \(seedFullDocFromCurrentDoc\(\)\)[\s\S]*return[\s\S]*const fetchId = \+\+activeDocFetchId[\s\S]*void loadFullDocForRoute\(fetchId, docPath\.value, docsLocale\.value\)[\s\S]*\}/)
    expect(page).toContain('async function loadActiveDocForRoute()')
    expect(page).toMatch(/const fetchId = \+\+activeDocFetchId[\s\S]*const path = docPath\.value[\s\S]*const locale = docsLocale\.value/)
    expect(page).toContain('const hasCachedBody = splitBody && cachedFullDoc !== undefined')
    expect(page).toMatch(/doc\.value = hasCachedBody \? cachedFullDoc : null[\s\S]*fullDoc\.value = hasCachedBody \? cachedFullDoc : null[\s\S]*isLoading\.value = !hasCachedBody[\s\S]*fullDocLoading\.value = splitBody && !hasCachedBody/)
    expect(page).toMatch(/const fullDocRequest = splitBody && !hasCachedBody[\s\S]*\? loadFullDocForRoute\(fetchId, path, locale\)[\s\S]*: null[\s\S]*try \{[\s\S]*requestJson<Record<string, any> \| null>\('\/api\/docs\/page'/)
    expect(page).toMatch(/requestJson<Record<string, any> \| null>\('\/api\/docs\/page'[\s\S]*body: splitBody \? '0' : '1'/)
    expect(page).toMatch(/if \(fetchId !== activeDocFetchId \|\| path !== docPath\.value \|\| locale !== docsLocale\.value\)[\s\S]*return/)
    expect(page).toMatch(/fullDoc\.value = cachedFullDoc/)
    expect(page).toContain('void fullDocRequest')
    expect(page).toMatch(/if \(import\.meta\.client\)[\s\S]*startFullDocFetchForRoute\(\)/)
    expect(page).toMatch(/watch\(requestKey, \(\) => \{[\s\S]*void loadActiveDocForRoute\(\)[\s\S]*\}\)/)
