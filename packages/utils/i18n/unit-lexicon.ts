export const officialDomainLexiconRegistry = new DomainLexiconRegistry(
  UNIT_LEXICON_ENTRIES,
);

export function isUnitLexiconMetadata(
  value: unknown,
): value is UnitLexiconMetadata {
  if (!value || typeof value !== "object" || Array.isArray(value)) return false;
  const metadata = value as Partial<UnitLexiconMetadata>;
  return (
    typeof metadata.symbol === "string" &&
    metadata.symbol.trim().length > 0 &&
    typeof metadata.category === "string" &&
    (UNIT_CATEGORIES as readonly string[]).includes(metadata.category)
  );
}

function unit(
  id: string,
  category: UnitCategory,
  symbol: string,
  zhLabel: string,
  enLabel: string,
  zhAliases: string[],
  enAliases: string[],
): DomainLexiconEntry {
  return {
    id,
    domain: "unit",
    source: "builtin",
    version: UNIT_LEXICON_VERSION,
    labels: {
      default: symbol,
      locales: {
        "zh-CN": zhLabel,
        "en-US": enLabel,
      },
    },
    aliases: {
      default: [symbol],
      locales: {
        "zh-CN": uniqueStrings([symbol, ...zhAliases]),
        "en-US": uniqueStrings([symbol, ...enAliases]),
      },
    },
    metadata: {
      category,
      symbol,
    } satisfies UnitLexiconMetadata,
  };
}

function uniqueStrings(values: readonly string[]): string[] {
  return Array.from(new Set(values));
}
